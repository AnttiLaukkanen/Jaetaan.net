    <p id='ukk_kysymys_1' class='info_teksti' onclick='ukk_vastaus_nayta_piiloon_1()'>
        <b>Voitteko antaa vinkkejä minkälainen on hyvä ilmoitus Jaetaan.netissä?</b></p>

    <br>

    <div id='ukk_vastaus_1'>

        <p class='info_teksti'>Otsikointi ja kuvat ovat tärkeitä.</p>
        <p class='info_teksti'>Etusivulla ilmoituksia selaava ihminen klikkaa jotain 
            tiettyä ilmoitusta todennäköisesti, koska otsikossa tai kuvassa oli jotain 
            häntä kiinnostavaa.</p>

        <br>

        <p class='info_teksti'>Otsikossa kannattaa välttää käyttämästä sanaa ilmainen ja samaa 
            tarkoittavia muita ilmaisuja.</p>
        <p class='info_teksti'>Otsikossa kannattaa välttää myös mainitsemasta kaupunkia, 
            aikaa tai paikkaa.</p>

        <br>

        <p class='info_teksti'>Jaetaan.netissä on ilmoituksia pelkästään ilmaisista asioista.</p>
        <p class='info_teksti'>Kaupunki, ja jos ilmoitettu, myös aika ja paikka näkyvät 
            etusivun listauksessa muutenkin.</p>

        <br>

        <p class='info_teksti'>Otsikosta näkyy rajallinen määrä merkkejä etusivun listauksessa.</p>

        <br>

        <p class='info_teksti'>Siksi yllä mainittujen sijaan otsikossa kannattaa kuvata ilmoitettavaa asiaa 
           muilta osin kiinnostavasti, kuvaavasti, lyhyesti ja ytimekkäästi.</p>

        <br>

        <p class='info_teksti'>Jos otsikon on tarpeellista olla pitkä, kirjoita kiinnostavimmat asiat 
            otsikon alkuun. Näin ne näkyvät etusivun listauksessa ja suurimmalla todennäköisyydellä 
            herättävät ilmoituksia selaavan kiinnostuksen.</p>

        <br>

        <p class='info_teksti'>Suosittelemme lämpimästi vähintään yhden valokuvan käyttämistä.</p>
        <p class='info_teksti'>Kuvallinen ilmoitus herättää enemmän huomiota kuin kuvaton.</p>

        <br>

        <p class='info_teksti'>Jos sopivaa kuvaa ilmoitettavasta asiasta ei löydy, jokin 
            yleisluontoinen kiva kuvakin saattaa toimia paremmin kuin täysin kuvaton ilmoitus.</p>

        <br>

        <p class='info_teksti'>Ja kuitenkin, jos minkäänmoista kuvaa ei löydy, ei hätää. 
            Kiinnostavasti muotoiltu otsikko toimii hyvin myös itsenäisesti.</p>
        <br>
        <p class='info_teksti'>Ilmoituksen sisältöosuudessa kaikki oleellinen ja kiinnostava 
            kannattaa kertoa alussa. Ilmoitettavaa asiaa voi tässä kuvailla pidemminkin. 
            Kannattaa kuitenkin miettiä, kuinka pitkiä kuvauksia ihmiset jaksavat lukea loppuun asti.</p>
        <br>
        <p class='info_teksti'>Muilta osin Lisää ilmoitus -lomake opastaa sinua tekemään 
            ilmoituksen, joka toimii hyvin Jaetaan.netissä.</p>
        <br>

