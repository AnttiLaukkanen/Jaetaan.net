    	<b>Järjestän live-musiikki tapahtuman, johon on vapaa pääsy. Tapahtumassa on 
    	    kuitenkin myös myytiä; mm. mehua ja leivonnaisia sekä käsitöitä. Voinko 
    	    ilmoittaa tapahtumasta Jaetaan.netissä?</b></p>

    <br>

    <div id='ukk_vastaus_2'>

        <p class='info_teksti'>Kyllä voit, koska tapahtumassa on tarjolla jotain 
        	ilmaiseksi – eli tässä tapauksessa live-musiikkia.</p> 

        <p class='info_teksti'>Jaetaan.netissä ilmoittamisen kannalta ei ole väliä, vaikka 
            samassa tapahtumassa on myös maksullista toimintaa.</p>

        <br>
       
    </div>

    <p id='ukk_kysymys_3' class='info_teksti' onclick='ukk_vastaus_nayta_piiloon_3()'><b>Järjestän tapahtuman, johon ei ole sisäänpääsymaksua. 
        Tapahtumassa on kuitenkin eteispalvelumaksu / narikkamaksu, joka on kaikille pakollinen. 
        Voinko ilmoittaa tapahtumasta Jaetaan.netissä?</b></p>

    <br>

    <div id='ukk_vastaus_3'>

        <p class='info_teksti'>Et voi.</p> 

        <p class='info_teksti'>Jaetaan.netissä voi ilmoittaa vain sellaisista tapahtumista, 
            joihin ei ole pääsymaksua muodossa eikä toisessa.</p>

        <p class='info_teksti'>Jos tapahtumassa on pakollinen narikka, sen tulee olla ilmainen. Jos taas 
    	    tapahtumassa on maksullinen narikka, sen tulee olla vapaaehtoinen.</p>

        <br>

    </div>

        <p id='ukk_kysymys_5' class='info_teksti' onclick='ukk_vastaus_nayta_piiloon_5()'>
        <b>Mitä tarkoittaa, kun ilmoituksen ilmoittajana on Jaetaan.net? 
            Entä Jaetaan.net automaatio?</b></p>

    <br>

        <div id='ukk_vastaus_5'>

        <p class='info_teksti'>Kun ilmoittajana on Jaetaan.net, tarkoittaa se sitä, että 
            joku Jaetaan.netin sisällön ylläpitäjistä on löytänyt tai saanut vinkin 
            sellaisesta julkisesta ilmaisesta asiasta, mikä hän arvelee kiinnostavan ihmisiä.</p> 
        <p class='info_teksti'>Ja hän on lisännyt tuon ilmoituksen sivustolle käsityönä.</p>

        <br>

        <p class='info_teksti'>Kun ilmoittajana on Jaetaan.net automaatio, tarkoittaa se sitä, 
            että ilmoitus on haettu <a class='info_linkki_2' target='_blank' href='https://api.hel.fi/linkedevents/v1/event/'>
            Helsingin kaupungin linked events -järjestelmästä</a> automaattisesti.</p>

        <br>

        <p class='info_teksti'>Linked events on Helsingin kaupungin ylläpitämä järjestelmä, johon 
            tällä hetkellä on koottu ennen kaikkea Helsingin kaupungin kulttuuritoimen, Visit Helsingin
            ja Helmet-kirjastojen tapahtumia.</p>
    
        <p class='info_teksti'>Lisäksi järjestelmästä löytyy jonkin verran Helsingin kaupungin 
            muiden virastojen ja laitosten järjestämiä tapahtumia.</p>

        <p class='info_teksti'>Linked events -järjestelmästä löytyy myös joitain 
            Helsingin kaupungin ja Helmet-kirjastojen ulkopuolisten julkisten ja yksityisten 
            tahojen järjestämiä tapahtumia.</p>

        <br>

        <p class='info_teksti'>Linked events -järjestelmästä voi kuka tahansa hakea 
            ilmoituksia vapaasti ja ilmaiseksi.</p>

        <br>

